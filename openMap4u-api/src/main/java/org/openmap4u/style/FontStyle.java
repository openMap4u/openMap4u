package org.openmap4u.style;


/**
 * The supported font styles.
 *
 * @author Michael Hadrbolec.
 *
 */
public enum FontStyle {

    /**
     * The normal font style (This is default).
     */
    NORMAL,
    /**
     * The italic font style.
     */
    ITALIC,
    /**
     * The oblique font style
     */
    OBLIQUE;
    
    /**
     * Default fontStyle.
     */
    public static final FontStyle DEFAULT = NORMAL;


}
